package Star_Pattern; 
 
public class pattern8 
{ 
public static void main(String[] args) { 
 
//* 
//* 
//*** 
//*** 
 
 
 
int star = 1; 
 
for(int i=1; i<=4; i++) //outer for loop --> rows 
{ 
for(int j=1; j<=star; j++) 
{ 
System.out.print("*"); 
} 
System.out.println(); 
star=star+2; 
 
} 
 
} 
 
} 