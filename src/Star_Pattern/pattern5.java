package Star_Pattern; 
 
public class pattern5 
{ 
public static void main(String[] args) 
{ 
 
//* 
//** 
//* 
//** 
 
 
int star=1; 
 
// 5<=4 
for(int i=1; i<=4; i++) //outer for loop --> rows 
{ 
// 5<=4 
for(int j=1; j<=star; j++) //inner for loop -- cols 
{ 
System.out.print("*"); 
} 
System.out.println(); 
star++; //5 
} 
 
 
//* 
//** 
//* 
//** 
// 
 
} 
 
} 
 
 
 
 
 
