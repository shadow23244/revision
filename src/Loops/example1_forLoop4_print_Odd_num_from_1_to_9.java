package Loops; 
 
public class example1_forLoop4_print_Odd_num_from_1_to_9 
{ 
public static void main(String[] args) 
{ 
 
//print num from 1 to 9 (odd num) 
//start num: 1 
//end num: 9 
//incr by 2 
 
// 
for(int i=1; i<=9; i=i+2) 
{ 
System.out.println(i); 
} 
 
} 
 
 
} 
 
 
 
 
 
