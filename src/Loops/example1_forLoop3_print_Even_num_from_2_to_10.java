package Loops; 
 
public class example1_forLoop3_print_Even_num_from_2_to_10 
{ 
public static void main(String[] args) 
{ 
 
//print num from 2 to 10 (even num) 
//start num: 2 
//end num: 10 
//incr by 2 
 
// 2 12<=10 12 
for(int i=2; i<=10; i=i+2) 
{ 
System.out.println(i); //2 4 6 8 10 
} 
 
} 
 
 
} 
 
 
 
 
 
 
