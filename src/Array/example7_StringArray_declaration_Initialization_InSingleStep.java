package Array; 
 
public class example7_StringArray_declaration_Initialization_InSingleStep 
{ 
public static void main(String[] args) 
{ 
 
 
String [] ar= {"rahul", "ganesh", "mahesh", "suresh"}; 
 
 
System.out.println(ar[3]); //suresh 
System.out.println(ar.length); //4 
 
 
System.out.println("---print all info---"); 
 
for(int i=0; i<=ar.length-1; i++) 
{ 
System.out.println(ar[i]); 
} 
 
 
} 
 
} 