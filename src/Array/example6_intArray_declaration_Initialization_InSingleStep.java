package Array; 
 
public class example6_intArray_declaration_Initialization_InSingleStep 
{ 
public static void main(String[] args) 
{ 
 
 
int [] ar= {30,20,40,10,50}; 
 
 
System.out.println(ar[3]); //10 
System.out.println(ar.length); //5 
 
 
System.out.println("---print all info---"); 
 
for(int i=0; i<=ar.length-1; i++) 
{ 
System.out.println(ar[i]); 
} 
 
 
} 
 
} 
 
 
 
